package com.ltg.shop.entity;

public class Shop {
    private Double applePrice = 8.0;
    private Double strawberryPrice = 13.0;
    private Double mangoPrice = 20.0;

    public Double getApplePrice() {
        return applePrice;
    }

    public void setApplePrice(Double applePrice) {
        this.applePrice = applePrice;
    }

    public Double getStrawberryPrice() {
        return strawberryPrice;
    }

    public void setStrawberryPrice(Double strawberryPrice) {
        this.strawberryPrice = strawberryPrice;
    }

    public Double getMangoPrice() {
        return mangoPrice;
    }

    public void setMangoPrice(Double mangoPrice) {
        this.mangoPrice = mangoPrice;
    }
}
